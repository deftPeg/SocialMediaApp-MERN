import mongoose from "mongoose";

// create User Schema
const postSchema = mongoose.Schema(
  {
    userId: {
      type: String,
      required: true,
    },
    firstName: {
      type: String,
      required: true,
    },
    lastName: {
      type: String,
      required: true,
    },
    location: String,
    description: String,
    picturePath: String,
    userPicturePath: String,
    likes: {
      type: Map,
      of: Boolean,
    },
    comments: {
      type: Array,
      default: [],
    },
  },
  //   automatic dates for creation, update etc....
  { timestamps: true }
);

// CREATE AND EXPORT USER MODEL
const Post = mongoose.model("Post", postSchema);

export default Post;
